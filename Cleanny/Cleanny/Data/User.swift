import SwiftUI

struct User{
    var name : String
    var totalPercentage : Double
    //분자
    var numerator : Double
    //분모
    var denominator : Int
    
    mutating func setName(name : String){
        self.name = name
    }
    func getName() -> String{
        return self.name
    }
    
    func getTotal() -> Double{
        return self.totalPercentage
    }
    
    mutating func setTotal(cleanings : [Cleaning]){
        numerator = 0
        denominator = 0
        
        for cleaning in cleanings {
            if(cleaning.activated){
                denominator += 1
                numerator += cleaning.percentage}
            else{
                
            }
        }
        
        
        
        if(denominator == 0){
            self.totalPercentage = -1
        }
        else{
            self.totalPercentage = (numerator / Double(denominator))
        }
        
    }
  
    
}
